    private void jTNomeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTNomeKeyTyped
        try {
            fornecedores = dao.listar("%" + jTNome.getText() + "%");
            while (tmFornecedor.getRowCount() > 0) {
                tmFornecedor.removeRow(0);
            }

            String endereco;
            for (int i = 0; i < fornecedores.size(); i++) {
                tmFornecedor.addRow(new String[]{null, null, null, null});
                // rua, numero - Centro - Cruzeiro/SP. CEP: 1223123123
                endereco = fornecedores.get(i).getRua() + ", "
                        + fornecedores.get(i).getNum() + " - "
                        + fornecedores.get(i).getBairro() + " - "
                        + fornecedores.get(i).getCidade() + "/"
                        + fornecedores.get(i).getEstado() + ". CEP: "
                        + fornecedores.get(i).getCep();
                tmFornecedor.setValueAt(fornecedores.get(i).getNome(), i, 0);
                tmFornecedor.setValueAt(fornecedores.get(i).getCnpj(), i, 1);
                tmFornecedor.setValueAt(fornecedores.get(i).getEmail(), i, 2);
                tmFornecedor.setValueAt(endereco, i, 3);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex);
        }
    }//GEN-LAST:event_jTNomeKeyTyped

